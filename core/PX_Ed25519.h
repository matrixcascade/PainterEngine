#ifndef PX_ED25519_H
#define PX_ED25519_H

#include "PX_Typedef.h"
#include "PX_Sha.h"

#define PX_ED25519_SEED_SIZE        32
#define PX_ED25519_PUBLIC_KEY_SIZE  32
#define PX_ED25519_PRIVATE_KEY_SIZE 64
#define PX_ED25519_SIGNATURE_SIZE   64
#define PX_ED25519_SCALAR_SIZE      32
#define PX_ED25519_SHARED_SECRET_SIZE 32

px_void PX_Ed25519_CreateKeypair(px_byte public_key[32], px_byte private_key[64], const px_byte seed[32]);
px_void PX_Ed25519_Sign(px_byte signature[64], const px_byte *message, px_int message_len, const px_byte public_key[32], const px_byte private_key[64]);
px_int  PX_Ed25519_Verify(const px_byte signature[64], const px_byte *message, px_int message_len, const px_byte public_key[32]);
px_void PX_Ed25519_AddScalar(px_byte *public_key, px_byte *private_key, const px_byte scalar[32]);
px_void PX_Ed25519_KeyExchange(px_byte shared_secret[32], const px_byte public_key[32], const px_byte private_key[64]);

#endif
